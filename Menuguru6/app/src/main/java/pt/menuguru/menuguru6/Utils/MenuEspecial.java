public class MenuEspecial {

    public String nome;
    public String db_id;
    public String restaurante_id;
    public String urlImage;
    public String nomeRestaurante;
    public String tipo;
    public String precoAntigo;
    public String precoNovo;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDb_id() {
        return db_id;
    }

    public void setDb_id(String db_id) {
        this.db_id = db_id;
    }

    public String getRestaurante_id() {
        return restaurante_id;
    }

    public void setRestaurante_id(String restaurante_id) {
        this.restaurante_id = restaurante_id;
    }

    public String getUrlImage() {
        return urlImage;
    }

    public void setUrlImage(String urlImage) {
        this.urlImage = urlImage;
    }

    public String getNomeRestaurante() {
        return nomeRestaurante;
    }

    public void setNomeRestaurante(String nomeRestaurante) {
        this.nomeRestaurante = nomeRestaurante;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getPrecoAntigo() {
        return precoAntigo;
    }

    public void setPrecoAntigo(String precoAntigo) {
        this.precoAntigo = precoAntigo;
    }

    public String getPrecoNovo() {
        return precoNovo;
    }

    public void setPrecoNovo(String precoNovo) {
        this.precoNovo = precoNovo;
    }

}
